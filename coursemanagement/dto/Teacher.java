package com.zsgs.coursemanagement.dto;

public class Teacher {
 private String userName;
 private String userLName;
 private String mobileNumber;
 private String emailAddress;
 private String teachingSubjectId;
 private String userId;
 public String getUserName() {
	return userName;
}
public void setUserName(String userName) {
	this.userName = userName;
}
public String getUserLName() {
	return userLName;
}
public void setUserLName(String userLName) {
	this.userLName = userLName;
}
public String getMobileNumber() {
	return mobileNumber;
}
public void setMobileNumber(String mobileNumber) {
	this.mobileNumber = mobileNumber;
}
public String getEmailAddress() {
	return emailAddress;
}
public void setEmailAddress(String emailAddress) {
	this.emailAddress = emailAddress;
}
public String getTeachingSubjectId() {
	return teachingSubjectId;
}
public void setTeachingSubjectId(String teachingSubjectId) {
	this.teachingSubjectId = teachingSubjectId;
}
public String getUserId() {
	return userId;
}
public void setUserId(String userId) {
	this.userId = userId;
}

}
